#include <iostream>
using namespace std;

int main () {
    int a = 8;
    float b;

// left shift
    b = a << 1;                                  // multiply by 2
    cout << b << endl;

// right shift                                   // divide by 2
    b = a >> 1;
    cout << b << endl;

// operators 
int c = 5;
cout << (++c) * (++c);

}
